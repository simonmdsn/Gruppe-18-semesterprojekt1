    public static void slowPrint(String message, long milliSecPerChar) {
        for (int i = 0; i < message.length(); i ++) {
            System.out.print(message.charAt(i));

            try {
                Thread.sleep(milliSecPerChar);
            }
            catch (InterruptedException e) {
                e.printStackTrace();
            }
        }
    }
    
